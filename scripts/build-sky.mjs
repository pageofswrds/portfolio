const normLon = (ra) => (ra < 0 ? ra + 360 : ra)
const r3 = (n) => Math.round(n * 1000) / 1000
const r2 = (n) => Math.round(n * 100) / 100

function centroid(coords) {
  let x = 0, y = 0, z = 0
  const DEG = Math.PI / 180
  for (const [lon, lat] of coords) {
    const la = lat * DEG, lo = lon * DEG
    x += Math.cos(la) * Math.cos(lo)
    y += Math.cos(la) * Math.sin(lo)
    z += Math.sin(la)
  }
  const lat = Math.atan2(z, Math.hypot(x, y)) * (180 / Math.PI)
  let lon = Math.atan2(y, x) * (180 / Math.PI)
  if (lon < 0) lon += 360
  return { lon: r3(lon), lat: r3(lat) }
}

const starsRaw = JSON.parse(readFileSync(join(dataDir, STARS_FILE), 'utf8')).features
const linesRaw = JSON.parse(readFileSync(join(dataDir, 'constellations.lines.json'), 'utf8')).features
const conRaw = JSON.parse(readFileSync(join(dataDir, 'constellations.json'), 'utf8')).features

const nameByDesig = {}
for (const f of conRaw) nameByDesig[f.properties.desig] = f.properties.name

const stars = starsRaw
  .filter((f) => f.properties.mag <= MAG_CUTOFF)
  .map((f) => {
    const [ra, dec] = f.geometry.coordinates
    const [r, g, b] = bvToRGB(f.properties.bv)
    const s = {
      id: `hip${f.id}`,
      lon: r3(normLon(ra)),
      lat: r3(dec),
      mag: r2(f.properties.mag),
      color: `rgb(${r}, ${g}, ${b})`,
    }
    if (NAMES[f.id]) s.name = NAMES[f.id]
    return s
  })
  .sort((a, b) => a.mag - b.mag)

const constellations = linesRaw.map((f) => {
  const paths = f.geometry.coordinates.map((path) => path.map(([ra, dec]) => [r3(normLon(ra)), r3(dec)]))
  const allVerts = paths.flat()
  return { name: nameByDesig[f.id] || f.id, paths, center: centroid(allVerts) }
})

const header = `// AUTO-GENERATED by scripts/build-sky.mjs — do not edit by hand.
// Source: d3-celestial (BSD-3-Clause), https://github.com/ofrohn/d3-celestial
// Star catalog filtered to magnitude <= ${MAG_CUTOFF} (${stars.length} stars);
// all ${constellations.length} constellation figures. Run: npm run build:sky
import type { Star, Constellation } from './constellations'
`

const body =
  header +
  `\nexport const STARS: Star[] = ${JSON.stringify(stars)}\n\n` +
  `export const CONSTELLATIONS: Constellation[] = ${JSON.stringify(constellations)}\n`

writeFileSync(outFile, body)
console.log(`Wrote ${stars.length} stars, ${constellations.length} constellations -> ${outFile}`)
console.log(`Named stars: ${stars.filter((s) => s.name).length}`)

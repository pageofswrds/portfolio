// Transforms the vendored d3-celestial data (BSD, github.com/ofrohn/d3-celestial)
// into src/sky/sky.generated.ts. Run with: npm run build:sky
//
// Stars come as [RA°, Dec°] with HIP ids and magnitudes (no proper names — we
// attach a curated set below). Constellation figures come as coordinate
// polylines keyed by IAU abbreviation, joined to full names via constellations.json.
import { readFileSync, writeFileSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const here = dirname(fileURLToPath(import.meta.url))
const dataDir = join(here, '..', 'data', 'd3-celestial')
const outFile = join(here, '..', 'src', 'sky', 'sky.generated.ts')

const MAG_CUTOFF = 7.0
const STARS_FILE = 'stars.8.json' // deep catalog (to mag 8); we filter below

// Approximate a star's RGB color from its B-V color index (blue -> white -> red).
function bvToRGB(bvRaw) {
  const anchors = [
    [-0.4, [155, 176, 255]],
    [-0.2, [170, 191, 255]],
    [0.0, [202, 216, 255]],
    [0.2, [248, 247, 255]],
    [0.4, [255, 250, 232]],
    [0.6, [255, 244, 214]],
    [0.8, [255, 230, 190]],
    [1.2, [255, 210, 160]],
    [1.6, [255, 185, 130]],
    [2.0, [255, 165, 110]],
  ]
  let v = parseFloat(bvRaw)
  if (!Number.isFinite(v)) v = 0.6
  v = Math.max(-0.4, Math.min(2.0, v))
  for (let i = 0; i < anchors.length - 1; i++) {
    const [b0, c0] = anchors[i]
    const [b1, c1] = anchors[i + 1]
    if (v <= b1) {
      const t = (v - b0) / (b1 - b0)
      return [0, 1, 2].map((k) => Math.round(c0[k] + (c1[k] - c0[k]) * t))
    }
  }
  return anchors[anchors.length - 1][1]
}

